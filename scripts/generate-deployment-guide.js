function safeText(v, ph) {
  if (v === undefined || v === null || v === "" || String(v) === "NaN") {
    return ph || "\u05F4\u05E0\u05D0 \u05DE\u05DC\u05D0\u05D5\u05F3"; // please fill in
  }
  return String(v);
}

// Body paragraph (Hebrew RTL)
function body(text, opts = {}) {
  return new Paragraph({
    bidirectional: true,
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: 312, after: 120, ...opts.spacing },
    children: [
      new TextRun({
        text: safeText(text),
        size: 22,
        color: c(P.body),
        font: { ascii: HEADING_FONT, eastAsia: HEADING_FONT, hAnsi: HEADING_FONT, cs: HEADING_FONT },
        rightToLeft: true,
      }),
    ],
    ...opts,
  });
}

// Body paragraph with mixed runs (for bold inline)
function bodyRuns(runs, opts = {}) {
  return new Paragraph({
    bidirectional: true,
    alignment: AlignmentType.JUSTIFIED,
    spacing: { line: 312, after: 120, ...opts.spacing },
    children: runs.map((r) => {
      if (typeof r === "string") {
        return new TextRun({
          text: r,
          size: 22,
          color: c(P.body),
          font: { ascii: HEADING_FONT, eastAsia: HEADING_FONT, hAnsi: HEADING_FONT, cs: HEADING_FONT },
          rightToLeft: true,
        });
      }
      return new TextRun({
        text: r.text,
        size: 22,
        color: c(r.color || P.body),
        bold: r.bold || false,
        italics: r.italic || false,
        font: r.mono
          ? { ascii: MONO_FONT, eastAsia: MONO_FONT, hAnsi: MONO_FONT, cs: MONO_FONT }
          : { ascii: HEADING_FONT, eastAsia: HEADING_FONT, hAnsi: HEADING_FONT, cs: HEADING_FONT },
        rightToLeft: !r.mono,
      });
    }),
    ...opts,
  });
}

