// Code block — monospace, light gray background, LTR
function codeBlock(code) {
  const lines = code.split("\n");
  const rows = lines.map((line) =>
    new TableRow({
      cantSplit: true,
      children: [
        new TableCell({
          width: { size: 100, type: WidthType.PERCENTAGE },
          margins: { top: 80, bottom: 80, left: 200, right: 200 },
          shading: { type: ShadingType.CLEAR, fill: "F4F8FC", color: "auto" },
          children: [
            new Paragraph({
              alignment: AlignmentType.LEFT,
              bidirectional: false,
              spacing: { line: 280 },
              children: [
                new TextRun({
                  text: line || " ",
                  size: 20, // 10pt
                  color: c(P.body),
                  font: { ascii: MONO_FONT, eastAsia: MONO_FONT, hAnsi: MONO_FONT, cs: MONO_FONT },
                }),
              ],
            }),
          ],
        }),
      ],
    })
  );

  return new Table({
    width: { size: 100, type: WidthType.PERCENTAGE },
    rows,
    borders: {
      top: { style: BorderStyle.SINGLE, size: 4, color: c(P.accent) },
      bottom: { style: BorderStyle.SINGLE, size: 4, color: c(P.accent) },
      left: { style: BorderStyle.SINGLE, size: 12, color: c(P.accent) },
      right: { style: BorderStyle.SINGLE, size: 4, color: "F4F8FC" },
      insideHorizontal: { style: BorderStyle.NONE, size: 0, color: "auto" },
      insideVertical: { style: BorderStyle.NONE, size: 0, color: "auto" },
    },
    visuallyRightToLeft: false,
  });
}

